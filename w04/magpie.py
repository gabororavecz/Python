num = int(input("Enter a number: "))

#statements
if num ==1:
        num = "One for sorrow"
        print(num) #output
elif num ==2:
        num = "Two for joy"
        print(num) #output
elif num ==3:
        num = "Three for a girl"
        print(num) #output
elif num ==4:
        num = "Four for a boy"
        print(num) #output
elif num ==5:
        num = "Five for silver"
        print(num) #output
elif num ==6:
        num = "Six for gold"
        print(num) #output
elif num ==7:
        num = "Seven for a secret never to be told"
        print(num) #output
else:
    print("Not a permitted number") #output

